function calculateHitTrees(data) {
  const rows = data.split(/\n/);
  let column = 0;
  let hitTrees = 0;
  rows.forEach((row) => {
    if (row[column] === '#') {
      hitTrees++;
    }
    switch (column) {
      case 28:
        column = 0;
        break;
      case 29:
        column = 1;
        break;
      case 30:
        column = 2;
        break;
      default:
        column += 3;
    }
  });
  console.log(hitTrees);
}

